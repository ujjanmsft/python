age = int(input("Enter your age: "))

if age >= 18:
    print("You are eligible to vote.")
    print("Please make sure you are registered to vote.")
    print("Please do vote on election day.")
else:
    print("You are not eligible to vote.")
    print("Please wait until you turn 18 to register and vote.")
    print("In the meantime, you can learn about the voting process.")